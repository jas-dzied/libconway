const WIDTH: u32 = 40;
const HEIGHT: u32 = 48;

pub fn gen() -> Vec<u32> {
    let mut rng = rand::thread_rng();
    let on = [
        (24, 0),
        (22, 1),
        (24, 1),
        (12, 2),
        (13, 2),
        (20, 2),
        (21, 2),
        (34, 2),
        (35, 2),
        (11, 3),
        (15, 3),
        (20, 3),
        (21, 3),
        (34, 3),
        (35, 3),
        (0, 4),
        (1, 4),
        (10, 4),
        (16, 4),
        (20, 4),
        (21, 4),
        (0, 5),
        (1, 5),
        (10, 5),
        (14, 5),
        (16, 5),
        (17, 5),
        (22, 5),
        (24, 5),
        (10, 6),
        (16, 6),
        (24, 6),
        (11, 7),
        (15, 7),
        (12, 8),
        (13, 8),
    ];
    let mut data = (0..(WIDTH * HEIGHT))
        .map(|_| false as u32)
        .collect::<Vec<_>>();
    for (x, y) in on {
        data[(y * WIDTH + x) as usize] = 1;
    }
    data
}
